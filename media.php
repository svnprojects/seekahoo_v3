						<p style="text-align:left; font-family:Robo-Regulor;font-size:18px;color:black;">Friday, January 16, 2015 4:00 am </br></br>
						By Philip Petrunak For The Times </br></br>
						
						MOON TWP. -- With spring improvement projects on the horizon,
						a pair of Allegheny County entrepreneurs have launched an app 
						aimed at matching homeowners with the right person for the job.</br></br>
						
						The advent of social media has allowed small businesses to connect
						with customers and promote their brand like never before, but Moon
						Township resident and independent contractor Zack Parme,
						28, said that building a customer base can be a long, difficult
						road for new contractors.</br></br>
						
						“For young (business owners), you’re in a dilemma,”
						he said. “You need to find new customers, but you don’t
						have the advertising budget and you also don’t have the
						years of word-of-mouth behind you.”</br></br>
						
						Parme, along with business partner and Moon resident Raj Gopal,
						have developed the Seekahoo home improvement app -- which gives
						Pittsburgh-area contractors a new tool in their belt for reaching
						customers and promoting their brand. The free app, which Parme
						dubbed the “Facebook for home improvement,”
						provides a one-stop platform for customers to
						search for and review the work of home improvement professionals.</br></br>
						
						Gopal said the name Seekahoo, coined by Parme, sums up the app’s goal perfectly.</br></br>
						
						“It’s about seeking out who you know and can trust,” Gopal said.</br></br>
						
						Seekahoo, which launched on Thanksgiving, is a mobile-only site currently
						available in the iTunes App store. The app asks businesses and customers to
						create a profile and works similarly to popular social media sites
						such as Facebook, LinkedIn and Twitter.
						Customers can find contractors in their 25-50-mile
						radius by searching hashtags for roofing, plumbing,
						electrical and other services.</br></br>
						
						Gopal said mobile was always at the forefront of their app designs.</br></br>
						“We felt like the idea of hashtags resonated with the culture right now,”
						Gopal said of the search method. “We wanted to make sure it was mobile
						(phone or tablet) focused. Most small-business owners and contractors are
						on the move, so they’re using their cellphones more than they are using a laptop or PC.”</br></br>
						
						App users can also follow friends and family members on the site to see
						which local contractors they have reviewed and recommend. Those reviews
						and images of the work a contractor has done are also linked to
						the businesses’ profile page, making it easy for a homeowner to vet a contractor.</br></br>
						
						Gopal said it’s this personal connection that sets the Seekahoo app apart
						from sites such as Yelp or Angie’s List.</br></br>
						
						“It’s not someone I don’t know telling me about the service,” Gopal said. “It’s friends and family;
						people I know and trust. We want to be pioneers in helping
						(customers) make effective decisions that are right for them.”</br></br>
						
						In addition to being consumer-friendly, Parme said the app would also
						help small-business owners grow their brand and reputation.</br></br>
						
						“At other sites, the leads are cold and you don’t really have a warm
						introduction (between the customer and the contractor),” Parme said.
						“If right off the bat as a contractor, you can say I know (someone) and
						you know them, too, and they’ve reviewed my work, it kind of breaks the ice a little bit.”</br></br>
						
						Although the app should benefit customers and contractors alike,
						the site does not yet produce revenue. Parme said that although
						the app will remain free for users and contractors, the two men
						have ideas for how to generate revenue in the future.</br></br>
						
						Parme and Gopal said the goal now is to grow not only customer awareness,
						but also the base of contractors and businesses registered for the site.
						Seekahoo is scheduled to have a presence at the Pittsburgh Home & Garden
						Show in March to attract more small businesses to the site.</br></br>
						
						Although the short-term goal is to promote the app in the local area,
						Gopal said the project could expand nationwide if it proves successful in Pittsburgh.
						Gopal said the group is also seeking out investors to help push the app forward.</br></br>
						
						“There is actually a very active and sizable start-up community in Pittsburgh,”
						Gopal added. “As we started to network, we realized there’s a lot of interest in (the app),
						and we’re optimistic. We’re kind of diving all in.”</br></br>
						
						The Seekahoo app is available in the iTunes app store and is scheduled
						to be rolled out to Android devices in February. Anyone interested in
						learning more about the app should visit <a href="https://www.seekahoo.com/" target="_blank" style="cursor:pointer;text-decoration:underline;">www.seekahoo.com</a> or search the app on iTunes.</br></br>
		
